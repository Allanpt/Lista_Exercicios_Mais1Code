// WHILE

// É uma estrutura de REPETIÇÃO que executa o código ENQUANTO a condição for verdadeira (TRUE).

// Exemplo: Imprima no console os numeros de 0 a 10

function deZeroADez () {
    let numero = 0        // Criei a váriável do tipo "Number"
    while(numero <= 10) { // Condição
        console.log(numero)
        numero++          // Soma(incremento) +1 na variável "numero"
    }
}
deZeroADez()