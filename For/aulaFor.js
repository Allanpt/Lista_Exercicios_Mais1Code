// FOR

// É uma estrutura de REPETIÇÃO que executa o código PARA um certo INTERVALO de valores se a condição for verdadeira (TRUE). 

// Exemplo: Imprima no console os numeros de 0 a 10

function deZeroADez () {
    for(let numero = 0; numero <= 10; numero++){ // Criei a váriável do tipo "Number", Condição, incremento +1.
        console.log(numero)
    }
}
deZeroADez()